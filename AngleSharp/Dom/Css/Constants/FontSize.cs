namespace AngleSharp.Dom.Css
{
    /// <summary>
    /// A listing of all possible pre-defined font-sizes.
    /// </summary>
    public enum FontSize : ushort
    {
        /// <summary>
        /// A custom font-size.
        /// </summary>
        Custom,
        /// <summary>
        /// Absolute xx-small.
        /// </summary>
        Tiny,
        /// <summary>
        /// Absolute x-small.
        /// </summary>
        Little,
        /// <summary>
        /// Smaller than the parent's font size.
        /// </summary>
        Smaller,
        /// <summary>
        /// Absolute small.
        /// </summary>
        Small,
        /// <summary>
        /// Absolute medium.
        /// </summary>
        Medium,
        /// <summary>
        /// Absolute large.
        /// </summary>
        Large,
        /// <summary>
        /// Larger than the parent's font size.
        /// </summary>
        Larger,
        /// <summary>
        /// Absolute x-large.
        /// </summary>
        Big,
        /// <summary>
        /// Absolute xx-large.
        /// </summary>
        Huge
    }
}
