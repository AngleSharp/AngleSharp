namespace AngleSharp.Network
{
    /// <summary>
    /// Represents the usable methods for transmitting HTTP forms.
    /// </summary>
    public enum HttpMethod : ushort
    {
        /// <summary>
        /// The GET method.
        /// </summary>
        Get,
        /// <summary>
        /// The POST method.
        /// </summary>
        Post,
        /// <summary>
        /// The PUT method.
        /// </summary>
        Put,
        /// <summary>
        /// The DELETE method.
        /// </summary>
        Delete
    }
}
